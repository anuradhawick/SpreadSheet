    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        // TODO add your handling code here:
        int c = tableSet1.getTable().getModel().getColumnCount();
        int r = tableSet1.getTable().getModel().getRowCount();
        if (r == 1000) {
            JOptionPane.showMessageDialog(null, "Maximum possible size reached", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Cell[][] C = new Cell[r + 1][c];
        for (int i = 0; i < r; i++) {
            System.arraycopy(((TableModel) tableSet1.getTable().getModel()).getData()[i], 0, C[i], 0, c);
        }
        tableSet1.getModel().updateData(C);
        tableSet1.updateUI();
        CellRenderer.renderAll(tableSet1);
    }//GEN-LAST:event_jButton12ActionPerformed

