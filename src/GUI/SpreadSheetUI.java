    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        int[] rows = tableSet1.getTable().getSelectedRows();
        int[] cols = tableSet1.getTable().getSelectedColumns();
        if (rows.length < 2) {
            JOptionPane.showMessageDialog(null, "Please select a wider range to using graphing tools", "Notification", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (cols.length != 2) {
            JOptionPane.showMessageDialog(null, "Number of columns must be 2", "Notification", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Plotter P = new Plotter(rows, cols, tableSet1);
        P.setIconImage(Toolkit.getDefaultToolkit().getImage(getClass().getResource("icons/Apps-Spreadsheet-App-icon.png")));
        Thread T = new Thread(P);
        T.start();

    }//GEN-LAST:event_jButton11ActionPerformed

